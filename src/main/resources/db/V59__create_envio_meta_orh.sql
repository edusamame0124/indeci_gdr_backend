ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

CREATE SEQUENCE SQ_GDR_ENVIO_META_ORH START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE TABLE GDR_ENVIO_META_ORH (
    ID_ENVIO_META_ORH NUMBER(19) NOT NULL,
    ID_GOAL NUMBER(19) NOT NULL,
    ID_ASSIGNMENT NUMBER(19) NOT NULL,
    ID_USUARIO_ENVIO NUMBER(19) NOT NULL,
    USUARIO_ENVIO VARCHAR2(60 CHAR) NOT NULL,
    ACTOR_FUNCIONAL_ENVIO VARCHAR2(40 CHAR) NOT NULL,
    COMENTARIO_ENVIO VARCHAR2(1000 CHAR),
    CODIGO_ESTADO_ENVIO VARCHAR2(20 CHAR) DEFAULT 'ENVIADO' NOT NULL,
    ESTADO_REGISTRO VARCHAR2(20 CHAR) DEFAULT 'ACTIVO' NOT NULL,
    FECHA_ENVIO TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_CREACION TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    FECHA_ACTUALIZACION TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    USUARIO_ACTUALIZACION VARCHAR2(60 CHAR) NOT NULL
);

