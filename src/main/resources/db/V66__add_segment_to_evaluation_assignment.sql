WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

ALTER TABLE GDR_EVALUATION_ASSIGNMENT ADD (
    ID_SEGMENT NUMBER(19)
);

UPDATE GDR_EVALUATION_ASSIGNMENT
SET ID_SEGMENT = (
    SELECT ID_SEGMENT
    FROM GDR_SEGMENT
    WHERE SEGMENT_CODE = 'GENERAL'
)
WHERE ID_SEGMENT IS NULL;

ALTER TABLE GDR_EVALUATION_ASSIGNMENT
    MODIFY ID_SEGMENT NOT NULL;

ALTER TABLE GDR_EVALUATION_ASSIGNMENT
    ADD CONSTRAINT FK_GDR_ASSIGN_SEGMENT
    FOREIGN KEY (ID_SEGMENT) REFERENCES GDR_SEGMENT (ID_SEGMENT);

CREATE INDEX IX_GDR_ASSIGN_SEGMENT
    ON GDR_EVALUATION_ASSIGNMENT (ID_SEGMENT);
