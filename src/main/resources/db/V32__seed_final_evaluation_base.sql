WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

MERGE INTO GDR_EVALUACION_FINAL target
USING (
    SELECT
        assignment.ID_ASSIGNMENT AS ID_ASSIGNMENT,
        50 AS CONSOLIDATED_SCORE,
        'Evaluacion base creada para validar el lote 3.' AS EVALUATION_COMMENT
    FROM GDR_EVALUATION_ASSIGNMENT assignment
    JOIN GDR_CYCLE cycle ON cycle.ID_CYCLE = assignment.ID_CYCLE
    JOIN HR_PERSON evaluated ON evaluated.ID_PERSON = assignment.ID_EVALUATED_PERSON
    WHERE cycle.STATUS = 'ACTIVE'
      AND evaluated.DOCUMENT_NUMBER = '70000002'
) source
ON (target.ID_ASSIGNMENT = source.ID_ASSIGNMENT)
WHEN MATCHED THEN
    UPDATE SET
        target.CONSOLIDATED_SCORE = source.CONSOLIDATED_SCORE,
        target.EVALUATION_COMMENT = source.EVALUATION_COMMENT,
        target.STATUS = 'ACTIVE',
        target.UPDATED_AT = SYSTIMESTAMP
WHEN NOT MATCHED THEN
    INSERT (
        ID_FINAL_EVALUATION,
        ID_ASSIGNMENT,
        CONSOLIDATED_SCORE,
        EVALUATION_COMMENT,
        STATUS,
        CREATED_AT,
        UPDATED_AT
    )
    VALUES (
        SQ_GDR_EVALUACION_FINAL.NEXTVAL,
        source.ID_ASSIGNMENT,
        source.CONSOLIDATED_SCORE,
        source.EVALUATION_COMMENT,
        'ACTIVE',
        SYSTIMESTAMP,
        SYSTIMESTAMP
    );

MERGE INTO GDR_DETALLE_PUNTAJE target
USING (
    SELECT
        evaluation.ID_FINAL_EVALUATION AS ID_FINAL_EVALUATION,
        goal.ID_GOAL AS ID_GOAL,
        100 AS ACHIEVED_VALUE,
        50 AS SCORE_VALUE,
        'Detalle base del lote 3.' AS DETAIL_COMMENT
    FROM GDR_EVALUACION_FINAL evaluation
    JOIN GDR_GOAL goal ON goal.ID_ASSIGNMENT = evaluation.ID_ASSIGNMENT
    WHERE goal.GOAL_TITLE = 'Meta institucional inicial'
) source
ON (target.ID_FINAL_EVALUATION = source.ID_FINAL_EVALUATION AND target.ID_GOAL = source.ID_GOAL)
WHEN MATCHED THEN
    UPDATE SET
        target.ACHIEVED_VALUE = source.ACHIEVED_VALUE,
        target.SCORE_VALUE = source.SCORE_VALUE,
        target.DETAIL_COMMENT = source.DETAIL_COMMENT,
        target.UPDATED_AT = SYSTIMESTAMP
WHEN NOT MATCHED THEN
    INSERT (
        ID_SCORE_DETAIL,
        ID_FINAL_EVALUATION,
        ID_GOAL,
        ACHIEVED_VALUE,
        SCORE_VALUE,
        DETAIL_COMMENT,
        CREATED_AT,
        UPDATED_AT
    )
    VALUES (
        SQ_GDR_DETALLE_PUNTAJE.NEXTVAL,
        source.ID_FINAL_EVALUATION,
        source.ID_GOAL,
        source.ACHIEVED_VALUE,
        source.SCORE_VALUE,
        source.DETAIL_COMMENT,
        SYSTIMESTAMP,
        SYSTIMESTAMP
    );
