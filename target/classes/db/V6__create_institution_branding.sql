WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

CREATE SEQUENCE SQ_SEC_INSTITUTION_BRANDING START WITH 1 INCREMENT BY 1 NOCACHE NOCYCLE;

CREATE TABLE SEC_INSTITUTION_BRANDING (
    ID_BRANDING NUMBER(19) NOT NULL,
    INSTITUTION_NAME VARCHAR2(200 CHAR) NOT NULL,
    MAIN_LOGO BLOB,
    MAIN_LOGO_MIME_TYPE VARCHAR2(100 CHAR),
    HEADER_LOGO BLOB,
    HEADER_LOGO_MIME_TYPE VARCHAR2(100 CHAR),
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

ALTER TABLE SEC_INSTITUTION_BRANDING
    ADD CONSTRAINT PK_SEC_INSTITUTION_BRANDING PRIMARY KEY (ID_BRANDING);

ALTER TABLE SEC_INSTITUTION_BRANDING
    ADD CONSTRAINT CK_SEC_INSTITUTION_BRANDING_STATUS
    CHECK (STATUS IN ('ACTIVE', 'INACTIVE'));

ALTER TABLE SEC_INSTITUTION_BRANDING
    ADD CONSTRAINT CK_SEC_INST_BRANDING_MAIN_MIME
    CHECK (
        (MAIN_LOGO IS NULL AND MAIN_LOGO_MIME_TYPE IS NULL)
        OR
        (MAIN_LOGO IS NOT NULL AND MAIN_LOGO_MIME_TYPE IN ('image/png', 'image/jpeg', 'image/webp', 'image/svg+xml'))
    );

ALTER TABLE SEC_INSTITUTION_BRANDING
    ADD CONSTRAINT CK_SEC_INST_BRANDING_HEADER_MIME
    CHECK (
        (HEADER_LOGO IS NULL AND HEADER_LOGO_MIME_TYPE IS NULL)
        OR
        (HEADER_LOGO IS NOT NULL AND HEADER_LOGO_MIME_TYPE IN ('image/png', 'image/jpeg', 'image/webp', 'image/svg+xml'))
    );

CREATE INDEX IX_SEC_INST_BRANDING_STATUS ON SEC_INSTITUTION_BRANDING (STATUS);

CREATE UNIQUE INDEX UX_SEC_INST_BRANDING_ONE_ACTIVE
    ON SEC_INSTITUTION_BRANDING (CASE WHEN STATUS = 'ACTIVE' THEN 1 END);
