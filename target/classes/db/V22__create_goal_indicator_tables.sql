WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

CREATE TABLE GDR_INDICATOR (
    ID_INDICATOR NUMBER(19) NOT NULL,
    INDICATOR_CODE VARCHAR2(40 CHAR) NOT NULL,
    INDICATOR_NAME VARCHAR2(150 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(500 CHAR),
    ID_VALUE_TYPE NUMBER(19) NOT NULL,
    ID_FORMULA NUMBER(19) NOT NULL,
    ID_SEGMENT NUMBER(19) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE GDR_GOAL (
    ID_GOAL NUMBER(19) NOT NULL,
    ID_ASSIGNMENT NUMBER(19) NOT NULL,
    ID_INDICATOR NUMBER(19) NOT NULL,
    GOAL_TITLE VARCHAR2(180 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(500 CHAR),
    EXPECTED_VALUE NUMBER(18,4) NOT NULL,
    WEIGHT NUMBER(5,2) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);
