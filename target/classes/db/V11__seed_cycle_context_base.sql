WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

MERGE INTO GDR_CYCLE target
USING (
    SELECT
        '2026' AS CYCLE_CODE,
        'Ciclo 2026' AS CYCLE_NAME
    FROM DUAL
) source
ON (target.CYCLE_CODE = source.CYCLE_CODE)
WHEN MATCHED THEN
    UPDATE SET
        target.CYCLE_NAME = source.CYCLE_NAME,
        target.STATUS = 'ACTIVE',
        target.UPDATED_AT = SYSTIMESTAMP
WHEN NOT MATCHED THEN
    INSERT (
        ID_CYCLE,
        CYCLE_CODE,
        CYCLE_NAME,
        STATUS,
        CREATED_AT,
        UPDATED_AT
    )
    VALUES (
        SQ_GDR_CYCLE.NEXTVAL,
        source.CYCLE_CODE,
        source.CYCLE_NAME,
        'ACTIVE',
        SYSTIMESTAMP,
        SYSTIMESTAMP
    );

UPDATE GDR_CYCLE
   SET STATUS = 'INACTIVE',
       UPDATED_AT = SYSTIMESTAMP
 WHERE CYCLE_CODE <> '2026'
   AND STATUS = 'ACTIVE';

MERGE INTO GDR_USER_CONTEXT_ASSIGNMENT target
USING (
    SELECT
        u.ID_USER AS ID_USER,
        c.ID_CYCLE AS ID_CYCLE,
        'CTX-ADMIN' AS CONTEXT_CODE,
        'Contexto institucional inicial' AS CONTEXT_NAME
    FROM SEC_USER u
    JOIN GDR_CYCLE c ON c.CYCLE_CODE = '2026'
    WHERE u.USERNAME = 'admin'
) source
ON (target.ID_USER = source.ID_USER AND target.ID_CYCLE = source.ID_CYCLE)
WHEN MATCHED THEN
    UPDATE SET
        target.CONTEXT_CODE = source.CONTEXT_CODE,
        target.CONTEXT_NAME = source.CONTEXT_NAME,
        target.STATUS = 'ACTIVE',
        target.ASSIGNED_AT = COALESCE(target.ASSIGNED_AT, SYSTIMESTAMP)
WHEN NOT MATCHED THEN
    INSERT (
        ID_USER_CONTEXT_ASSIGNMENT,
        ID_USER,
        ID_CYCLE,
        CONTEXT_CODE,
        CONTEXT_NAME,
        STATUS,
        ASSIGNED_AT
    )
    VALUES (
        SQ_GDR_USER_CONTEXT_ASSIGNMENT.NEXTVAL,
        source.ID_USER,
        source.ID_CYCLE,
        source.CONTEXT_CODE,
        source.CONTEXT_NAME,
        'ACTIVE',
        SYSTIMESTAMP
    );
