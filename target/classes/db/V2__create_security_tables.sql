WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

CREATE TABLE SEC_USER (
    ID_USER NUMBER(19) NOT NULL,
    USERNAME VARCHAR2(60 CHAR) NOT NULL,
    PASSWORD_HASH VARCHAR2(255 CHAR) NOT NULL,
    EMAIL VARCHAR2(150 CHAR) NOT NULL,
    DISPLAY_NAME VARCHAR2(150 CHAR) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    FAILED_ATTEMPTS NUMBER(5) DEFAULT 0 NOT NULL,
    LOCKED_UNTIL TIMESTAMP(6),
    LAST_LOGIN_AT TIMESTAMP(6),
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE SEC_ROLE (
    ID_ROLE NUMBER(19) NOT NULL,
    ROLE_CODE VARCHAR2(40 CHAR) NOT NULL,
    ROLE_NAME VARCHAR2(100 CHAR) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE SEC_USER_ROLE (
    ID_USER_ROLE NUMBER(19) NOT NULL,
    ID_USER NUMBER(19) NOT NULL,
    ID_ROLE NUMBER(19) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    ASSIGNED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE SEC_REFRESH_TOKEN (
    ID_REFRESH_TOKEN NUMBER(19) NOT NULL,
    ID_USER NUMBER(19) NOT NULL,
    TOKEN_VALUE VARCHAR2(1000 CHAR) NOT NULL,
    EXPIRES_AT TIMESTAMP(6) NOT NULL,
    REVOKED_AT TIMESTAMP(6),
    CLIENT_IP VARCHAR2(64 CHAR),
    USER_AGENT VARCHAR2(500 CHAR),
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);
