CREATE TABLE GDR_CYCLE (
    ID_CYCLE NUMBER(19) NOT NULL,
    CYCLE_CODE VARCHAR2(40 CHAR) NOT NULL,
    CYCLE_NAME VARCHAR2(150 CHAR) NOT NULL,
    START_DATE DATE,
    END_DATE DATE,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE GDR_USER_CONTEXT_ASSIGNMENT (
    ID_USER_CONTEXT_ASSIGNMENT NUMBER(19) NOT NULL,
    ID_USER NUMBER(19) NOT NULL,
    ID_CYCLE NUMBER(19) NOT NULL,
    CONTEXT_CODE VARCHAR2(60 CHAR),
    CONTEXT_NAME VARCHAR2(180 CHAR),
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    ASSIGNED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);
