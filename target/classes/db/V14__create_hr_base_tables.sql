CREATE TABLE HR_ORG_UNIT (
    ID_ORG_UNIT NUMBER(19) NOT NULL,
    UNIT_CODE VARCHAR2(40 CHAR) NOT NULL,
    UNIT_NAME VARCHAR2(150 CHAR) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE HR_PERSON (
    ID_PERSON NUMBER(19) NOT NULL,
    DOCUMENT_NUMBER VARCHAR2(20 CHAR) NOT NULL,
    EMAIL VARCHAR2(150 CHAR),
    DISPLAY_NAME VARCHAR2(150 CHAR) NOT NULL,
    ID_ORG_UNIT NUMBER(19) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP(6) DEFAULT SYSTIMESTAMP NOT NULL
);

