WHENEVER SQLERROR EXIT FAILURE ROLLBACK
ALTER SESSION SET CURRENT_SCHEMA = &&APP_DB_SCHEMA;

MERGE INTO GDR_RESULTADO target
USING (
    SELECT
        evaluation.ID_ASSIGNMENT AS ID_ASSIGNMENT,
        evaluation.ID_FINAL_EVALUATION AS ID_FINAL_EVALUATION,
        evaluation.CONSOLIDATED_SCORE AS CONSOLIDATED_SCORE
    FROM GDR_EVALUACION_FINAL evaluation
    JOIN GDR_EVALUATION_ASSIGNMENT assignment ON assignment.ID_ASSIGNMENT = evaluation.ID_ASSIGNMENT
    JOIN HR_PERSON evaluated ON evaluated.ID_PERSON = assignment.ID_EVALUATED_PERSON
    WHERE evaluated.DOCUMENT_NUMBER = '70000002'
) source
ON (target.ID_ASSIGNMENT = source.ID_ASSIGNMENT)
WHEN MATCHED THEN
    UPDATE SET
        target.ID_FINAL_EVALUATION = source.ID_FINAL_EVALUATION,
        target.CONSOLIDATED_SCORE = source.CONSOLIDATED_SCORE,
        target.STATUS = 'ACTIVE',
        target.UPDATED_AT = SYSTIMESTAMP
WHEN NOT MATCHED THEN
    INSERT (
        ID_RESULT,
        ID_ASSIGNMENT,
        ID_FINAL_EVALUATION,
        CONSOLIDATED_SCORE,
        STATUS,
        CREATED_AT,
        UPDATED_AT
    )
    VALUES (
        SQ_GDR_RESULTADO.NEXTVAL,
        source.ID_ASSIGNMENT,
        source.ID_FINAL_EVALUATION,
        source.CONSOLIDATED_SCORE,
        'ACTIVE',
        SYSTIMESTAMP,
        SYSTIMESTAMP
    );
